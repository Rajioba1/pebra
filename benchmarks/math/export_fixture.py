"""Offline exporter: run the REAL ignition loop and freeze a normalized prediction-errors CSV.

This is the data-export side of the Tauri-style validation. The independent reference and PEBRA
metric artifacts are generated by separate modules from the committed CSV.

HONEST SCOPE: the predicted probabilities here are still authored — what is "real" is the PIPELINE
they flow through (persist -> outcome -> label join -> censoring -> measure), the row schema, and the
observed/censored split. It is NOT a real data DISTRIBUTION; that needs the JIT/SZZ corpus (deferred).
So this strengthens wiring + realism + data/code decoupling, NOT formula correctness (closed-form math
is input-agnostic).

Regenerate with::

    python -m benchmarks.math.export_fixture            # writes data/prediction_errors.csv + reference
    nox -s bench-math-regen                             # same, in a clean env

Determinism: rows are stripped of all chain/lifecycle fields (recorded_at, *_hash, shadow_mode, …),
sorted by a fixed key, and floats formatted ``%.15g`` with ``\n`` line terminators, so the CSV is
byte-identical on every regen across platforms.
"""

from __future__ import annotations

import argparse
import csv
import io
import json
import os
import tempfile
from pathlib import Path

from benchmarks.math import reference_metrics as ref

DATA_DIR = Path(__file__).parent / "data"
FIXTURE_CSV = DATA_DIR / "prediction_errors.csv"

REPO_ID = "bench"

EXPORT_COLUMNS = (
    "target_type", "target_name", "predicted_probability", "predicted_value",
    "actual_outcome", "actual_value", "outcome_label_status",
)

# --- pure helpers (CI-tested) ------------------------------------------------------------------


def _num(value) -> float:
    """Sort coercion: NULL/empty numeric fields sort before any real value."""
    if value is None or value == "":
        return float("-inf")
    return float(value)


def _strip_row(row: dict) -> dict:
    """Keep only the export columns — drop chain (prev_hash/row_hash), derived (brier_error/log_loss/
    squared_error), and lifecycle (shadow_mode/calibration_scope/assessment_id/guidance) fields."""
    return {c: row.get(c) for c in EXPORT_COLUMNS}


def _sort_key(row: dict):
    return (
        row["target_type"], row["target_name"],
        _num(row.get("predicted_probability")), _num(row.get("predicted_value")),
    )


def _sort_rows(rows: list[dict]) -> list[dict]:
    return sorted(rows, key=_sort_key)


def _fmt(value) -> str:
    """Platform-stable cell formatting. ``%.15g`` round-trips an IEEE-754 double without trailing-zero
    noise; None -> empty; ints stay integral."""
    if value is None or value == "":
        return ""
    if isinstance(value, bool):
        return str(int(value))
    if isinstance(value, int):
        return str(value)
    if isinstance(value, float):
        return "%.15g" % value
    return str(value)


def rows_to_csv(rows: list[dict]) -> str:
    out = io.StringIO()
    writer = csv.writer(out, lineterminator="\n")  # \n (not \r\n) for cross-platform byte identity
    writer.writerow(EXPORT_COLUMNS)
    for row in (_strip_row(r) for r in _sort_rows(rows)):
        writer.writerow([_fmt(row[c]) for c in EXPORT_COLUMNS])
    return out.getvalue()


def _load_json(path: Path) -> dict:
    return json.loads(Path(path).read_text(encoding="utf-8"))


def _atomic_write(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    fd, tmp = tempfile.mkstemp(dir=str(path.parent), suffix=".tmp")
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="") as fh:
            fh.write(text)
        os.replace(tmp, path)
    finally:
        if os.path.exists(tmp):
            os.remove(tmp)


# --- offline ignition driver (NOT run in CI) ---------------------------------------------------


def _run_scenarios() -> list[dict]:
    """Drive the real assess->outcome->measure loop over authored, diverse inputs covering all three
    target types, and return the observed prediction_errors rows. Imports the engine lazily so the pure
    helpers above stay importable without the adapter stack."""
    from pebra.adapters.learning_store import LearningStore
    from pebra.adapters.store.db import SqliteStore
    from pebra.app import learning_controller as lc
    from pebra.core.constants import ActionStatus, Decision, RiskMode
    from pebra.core.models import AssessmentResult

    def _point(store, *, target_type, target_name, predicted_value, detail, guardrails=None):
        result = AssessmentResult(
            recommended_decision=Decision.PROCEED, requires_confirmation=False,
            action_status=ActionStatus.PENDING, risk_mode=RiskMode.NORMAL,
            scores={"benefit": 0.5}, repo_id=REPO_ID, repo_root="/x",
        )
        asm = store.persist_assessment(
            result, {"task": "t", "action_id": target_name},
            predictions=[{
                "action_id": target_name, "target_type": target_type,
                "target_name": target_name, "predicted_value": predicted_value, "features": {},
            }],
        )
        if guardrails is not None:
            store.persist_guardrails(asm, guardrails)
        store.record_outcome(asm, "completed", detail)
        lc.measure_learning(asm, store=store, learning_port=LearningStore(store))

    # risk_binary (p_success): spread across the [0,1] range with a realistic mix of outcomes.
    risk = [
        (0.08, False), (0.15, False), (0.27, False), (0.42, True),
        (0.55, False), (0.68, True), (0.85, True), (0.93, True),
    ]
    # benefit_binary (immediate_benefit_realized).
    benefit_bin = [(0.20, False), (0.45, False), (0.60, True), (0.80, True), (0.90, True)]
    # benefit_continuous (measured_benefit): (predicted, actual).
    benefit_cont = [(0.30, 0.25), (0.10, 0.12), (0.80, 0.77), (0.50, 0.61)]

    with tempfile.TemporaryDirectory() as tmp:
        store = SqliteStore(str(Path(tmp) / "bench.db"))
        for p, y in risk:
            _point(store, target_type="risk_binary", target_name="p_success",
                    predicted_value=p, detail={"actual_success": y})
        for p, y in benefit_bin:
            _point(store, target_type="benefit_binary", target_name="immediate_benefit_realized",
                    predicted_value=p, detail={"benefit_realized": y})
        for p, a in benefit_cont:
            _point(store, target_type="benefit_continuous", target_name="measured_benefit",
                    predicted_value=p, detail={"actual_success": True},
                    guardrails={"measured_benefit": a, "measured_benefit_deltas": {}})
        rows = [r for r in store.load_prediction_errors(REPO_ID)
                if r["outcome_label_status"] == "observed"]
        store.close()
    return rows


def export() -> str:
    """Run the loop, validate coverage, and return CSV text without writing."""
    from benchmarks.math import fixture_loader as fl

    rows = _run_scenarios()
    csv_text = rows_to_csv(rows)
    # Validate coverage off the SAME bytes the loader will read, so the committed artifact is provably
    # loadable and rich enough (>=8 binary, >=4 continuous, >=3 populated ECE bins).
    fd, tmp = tempfile.mkstemp(suffix=".csv")
    try:
        with os.fdopen(fd, "w", encoding="utf-8", newline="") as fh:
            fh.write(csv_text)
        binary, cont = fl.load_all_pairs(Path(tmp))
    finally:
        os.remove(tmp)
    if len(binary) < 8:
        raise RuntimeError(f"need >=8 observed binary rows, got {len(binary)}")
    if len(cont) < 4:
        raise RuntimeError(f"need >=4 observed continuous rows, got {len(cont)}")
    # Count populated bins on the edge convention the metrics use (PEBRA's bisect_right and the numpy
    # oracle agree on this binning), to confirm the fixture has real ECE spread before it is committed.
    import numpy as np
    _edges = np.linspace(0.0, 1.0, 11)[1:-1]
    populated_bins = len({
        int(np.clip(np.digitize(p, _edges, right=False), 0, 9)) for p, _ in binary
    })
    if populated_bins < 3:
        raise RuntimeError(f"need >=3 populated ECE bins, got {populated_bins}")
    return csv_text


def main(argv: list[str] | None = None) -> int:
    argparse.ArgumentParser(description="Export the benchmark prediction-errors fixture.").parse_args(argv)
    csv_text = export()
    _atomic_write(FIXTURE_CSV, csv_text)
    ref.write_reference_artifact(FIXTURE_CSV)
    print(f"wrote {FIXTURE_CSV} ({csv_text.count(chr(10)) - 1} rows)")
    print(f"wrote {ref.REFERENCE_METRICS_JSON}")
    return 0


if __name__ == "__main__":  # pragma: no cover - thin CLI shell
    raise SystemExit(main())
